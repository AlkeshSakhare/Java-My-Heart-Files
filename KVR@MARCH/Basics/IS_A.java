//IS_A --->obtaing the features of another Class


class c1
{
	void show()
	{
		System.out.println("C1 show");	
	}
}

class c2  extends c1   //IS-A relation ship
{
	void disp()
	{
		System.out.println("C2 disp");	
	}
}


class IS_A 
{
	public static void main(String[] args) 
	{
        c2 ob = new c2();
		ob.disp();
		ob.show();
	}
}
