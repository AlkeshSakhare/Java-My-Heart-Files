package samepack;


public class  SameBaseClass
{
			private int pri = 10;
					int	def =  20;
			public int pub = 30;
			protected int pro  = 40;

			public SameBaseClass()
	{
				System.out.println("");
				System.out.println("PRIVATE ----:>"+pri);
				System.out.println("DEFAULT ----:>"+def);
				System.out.println("PUBLIC ----:>"+pub);
				System.out.println("PROTECTED ----:>"+pro);
				System.out.println("");
			
			}


}
