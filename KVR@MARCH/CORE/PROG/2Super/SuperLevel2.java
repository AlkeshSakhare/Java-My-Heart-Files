class Bc
{

	Bc()
	{
		System.out.println("Bc Default");
	}

	Bc(int x)
	{

		int a = x;
		System.out.println("Bc param"+a);
	}
}

class Dc extends Bc
{

	Dc()
	{
		super(10);
		System.out.println("Dc Default");
	}
}


