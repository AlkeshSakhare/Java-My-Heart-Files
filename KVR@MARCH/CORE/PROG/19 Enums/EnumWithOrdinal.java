
enum Alpha
{
	a,b,c,d,e;
}


class EnumWithOrdinal
{
	
	public static void main(String[] args) 
	{
	System.out.println("Ordinal of a		:"+(Alpha.a).ordinal());
	System.out.println("Ordinal of b		:"+(Alpha.b).ordinal());
	System.out.println("Ordinal of c		:"+(Alpha.c).ordinal());
	System.out.println("Ordinal of d		:"+(Alpha.d).ordinal());
	System.out.println("Ordinal of e		:"+(Alpha.e).ordinal());
	}

}

/*
OutPut:
------
a
*/


